# This is an example of an if-else statement in Python
x = 10
# The variable 'x' is assigned the value of 10

# The if statement checks if x is greater than 10
if x > 10:
    # If x is greater than 10, print the following message
    print("x is greater than 10")
else:
    # If x is less than or equal to 10, print the following message
    print("x is less than or equal to 10")

# This is an example of an if-elif-else statement in Python
x = 10
# The variable 'x' is assigned the value of 10

# The if statement checks if x is greater than 10
if x > 10:
    # If x is greater than 10, print the following message
    print("x is greater than 10")
# The elif statement checks if x is equal to 10
elif x == 10:
    # If x is equal to 10, print the following message
    print("x is equal to 10")
else:
    # If x is less than 10, print the following message
    print("x is less than 10")